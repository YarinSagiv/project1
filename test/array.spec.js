var expect = require('chai').expect;

describe(title = 'Array', fn = () => {
    describe(title = '#sort', fn = () => {
        it(title = 'should sorting array by name', fn = () => {
            var names = ['Mor', 'Yarin', 'Noa', 'Ofir'];
            expect(names.sort()).to.be.eql(value = ['Mor', 'Noa', 'Ofir', 'Yarin']);
        })

    })
})


